    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        consultar();
        String tablaVal[] = text.split("\t");
        //  Extraer la fecha actual
        DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy"); 
        Date date = new Date(); 
        String fechaActual = dateFormat.format(date);
        //  Generar la tabla del PDF
        PdfPTable tabla = new PdfPTable(6); //  PdfTable(Numero de columnas)
        String ruta = txtRuta.getText();
        try{
            FileOutputStream archivo = new FileOutputStream(ruta+".pdf");
            Document documento = new Document();
            PdfWriter.getInstance(documento, archivo);
            
            // Realizar consulta en la base de datos y extraer los datos 
            documento.open();
            String titulo = "EL FUTURO DE MÃ‰XICO\n\n";
            String descripcion ="Reporte de los usuarios registrados en el sistema "
                    + "\n Fecha: "+fechaActual+".\n\n";
            documento.add(new Paragraph(titulo));
            documento.add(new Paragraph(descripcion));
            //  Realiza las tablas
            tabla.addCell("ID");
            tabla.addCell("Nombre");
            tabla.addCell("Apellido Paterno");
            tabla.addCell("Apellido Materno");
            tabla.addCell("Edad");
            tabla.addCell("Telefono");
            for(int i = 0; i < tablaVal.length; i++){
                tabla.addCell(tablaVal[i]);
            }
            documento.add(tabla);
            documento.close();
            
            JOptionPane.showMessageDialog(null, "PDF correctamente creado");
        }catch(java.lang.NullPointerException e){
            System.out.println("ERROR. PDF no creado: "+e);
        }catch(Exception e){
            System.out.println("ERROR. PDF no creado: "+e);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

