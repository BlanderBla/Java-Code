    private void consultar(){
        try{
            Connection conn = DriverManager.getConnection(url,usuarioBD,passwordBD);
            String querry = "SELECT * FROM USUARIO";
            Statement sts = conn.createStatement();
            ResultSet rs = sts.executeQuery(querry);
            
            while(rs.next()){
                id = rs.getInt("ID_USUARIO");
                nombre = rs.getString("NOMBRE_USUARIO");
                apellidoP = rs.getString("APELLIDO_PATERNO");
                apellidoM = rs.getString("APELLIDO_MATERNO");
                edad = rs.getInt("EDAD_USUARIO");
                telefono = rs.getString("TELEFONOUSUARIO");
                text += id+"\t"+nombre+"\t"+apellidoP+"\t"+apellidoM+"\t"+edad+"\t"+telefono+"\t";
            }

            System.out.println("Proceso terminado");
            sts.close();
            conn.close();

        }catch (SQLException ex) {
            System.out.println(ex.getNextException());
            System.out.println("No se establecio conexion");
            System.out.println(ex);
        }
    }
    
